# Q.1 Write a program to create a NumPy 1D-array with 5 elements and perform basic 
# operations like: 
import numpy as np
arr=np.array([1,2,3,4,5])
print(f"Array is {arr}")
# a) Addition of 2 in all the element
add=arr+2
print(f"After adding 2 in all elements: {add}")

# b) Multiply 3 with all the elements 
mult=arr*3
print(f"After multiplying 3 with all elements: {mult}")

# c) Divide every element by 2 
div=arr/2
print(f"After dividing every element by 2: {div}")